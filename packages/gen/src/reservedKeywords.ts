export const RESERVED_KEYWORDS: ReadonlySet<string> = new Set([
  // ECMAScript reserved words
  "break",
  "case",
  "catch",
  "continue",
  "debugger",
  "default",
  "delete",
  "do",
  "else",
  "finally",
  "for",
  "function",
  "if",
  "in",
  "instanceof",
  "new",
  "return",
  "switch",
  "this",
  "throw",
  "try",
  "typeof",
  "var",
  "void",
  "while",
  "with",
  // Strict mode reserved
  "class",
  "const",
  "enum",
  "export",
  "extends",
  "import",
  "super",
  // Strict mode future reserved
  "implements",
  "interface",
  "let",
  "package",
  "private",
  "protected",
  "public",
  "static",
  "yield",
  // TypeScript keywords
  "abstract",
  "declare",
  "is",
  "module",
  "namespace",
  "require",
  "type",
  // Dangerous identifiers
  "constructor",
  "prototype",
  "__proto__",
  "arguments",
  "eval",
]);

export function isReservedKeyword(identifier: string): boolean {
  return RESERVED_KEYWORDS.has(identifier);
}
