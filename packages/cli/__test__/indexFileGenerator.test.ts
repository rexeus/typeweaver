import fs from "node:fs";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { afterEach, describe, expect, test } from "vitest";
import { generateIndexFiles } from "../src/generators/indexFileGenerator.js";
import type { IndexFileGenerationContext } from "../src/generators/indexFileGenerator.js";

type GeneratorContextOptions = {
  readonly outputDir: string;
  readonly generatedFiles: readonly string[];
};

const templateDir = fileURLToPath(
  new URL("../src/generators/templates", import.meta.url)
);

function aGeneratorContextWith({
  outputDir,
  generatedFiles,
}: GeneratorContextOptions): IndexFileGenerationContext {
  return {
    outputDir,
    getGeneratedFiles: () => [...generatedFiles],
  };
}

function expectedIndexFile(indexPaths: readonly string[]): string {
  const exports = indexPaths
    .map(indexPath => `\nexport * from "${indexPath}";\n`)
    .join("");

  return `/* eslint-disable */
/**
 * This file was automatically generated by typeweaver.
 * DO NOT EDIT. Instead, modify the source definition file and generate again.
 *
 * @generated by @rexeus/typeweaver
 */

${exports}
`;
}

function readOutputFile(outputDir: string, ...pathSegments: string[]): string {
  return fs.readFileSync(path.join(outputDir, ...pathSegments), "utf8");
}

describe("generateIndexFiles", () => {
  const tempDirs: string[] = [];

  afterEach(() => {
    for (const tempDir of tempDirs) {
      fs.rmSync(tempDir, { recursive: true, force: true });
    }

    tempDirs.length = 0;
  });

  function createTempOutputDir(): string {
    const tempDir = fs.mkdtempSync(path.join(os.tmpdir(), "typeweaver-index-"));
    tempDirs.push(tempDir);

    return tempDir;
  }

  test("creates resource barrels and a root barrel for generated files", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: [
          "todo/GetTodoRequest.ts",
          "todo/GetTodoResponse.ts",
          "responses/TodoResponse.ts",
        ],
      })
    );

    expect(readOutputFile(outputDir, "todo", "index.ts")).toBe(
      expectedIndexFile(["./GetTodoRequest.js", "./GetTodoResponse.js"])
    );
    expect(readOutputFile(outputDir, "responses", "index.ts")).toBe(
      expectedIndexFile(["./TodoResponse.js"])
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./responses/index.js", "./todo/index.js"])
    );
  });

  test("keeps a generated group index file instead of overwriting it", () => {
    const outputDir = createTempOutputDir();
    const existingTodoIndex = path.join(outputDir, "todo", "index.ts");
    fs.mkdirSync(path.dirname(existingTodoIndex), { recursive: true });
    fs.writeFileSync(existingTodoIndex, "export const ownedByPlugin = true;\n");

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["todo/index.ts", "todo/GetTodoResponse.ts"],
      })
    );

    expect(readOutputFile(outputDir, "todo", "index.ts")).toBe(
      "export const ownedByPlugin = true;\n"
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./todo/index.js"])
    );
  });

  test("groups lib namespace files under their two-segment namespace", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["lib/clients/BaseClient.ts", "lib/types/Shared.ts"],
      })
    );

    expect(readOutputFile(outputDir, "lib", "clients", "index.ts")).toBe(
      expectedIndexFile(["./BaseClient.js"])
    );
    expect(readOutputFile(outputDir, "lib", "types", "index.ts")).toBe(
      expectedIndexFile(["./Shared.js"])
    );
    expect(fs.existsSync(path.join(outputDir, "lib", "index.ts"))).toBe(false);
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./lib/clients/index.js", "./lib/types/index.js"])
    );
  });

  test("keeps a generated lib barrel and generates sibling namespace barrels", () => {
    const outputDir = createTempOutputDir();
    const existingLibIndex = path.join(outputDir, "lib", "index.ts");
    fs.mkdirSync(path.dirname(existingLibIndex), { recursive: true });
    fs.writeFileSync(existingLibIndex, "export const ownedByPlugin = true;\n");

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["lib/index.ts", "lib/clients/BaseClient.ts"],
      })
    );

    expect(readOutputFile(outputDir, "lib", "index.ts")).toBe(
      "export const ownedByPlugin = true;\n"
    );
    expect(
      fs.existsSync(path.join(outputDir, "lib", "index", "index.ts"))
    ).toBe(false);
    expect(readOutputFile(outputDir, "lib", "clients", "index.ts")).toBe(
      expectedIndexFile(["./BaseClient.js"])
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./lib/clients/index.js", "./lib/index.js"])
    );
  });

  test("keeps a generated lib namespace index file instead of overwriting it", () => {
    const outputDir = createTempOutputDir();
    const existingClientIndex = path.join(
      outputDir,
      "lib",
      "clients",
      "index.ts"
    );
    fs.mkdirSync(path.dirname(existingClientIndex), { recursive: true });
    fs.writeFileSync(
      existingClientIndex,
      "export const ownedByPlugin = true;\n"
    );

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["lib/clients/index.ts", "lib/clients/BaseClient.ts"],
      })
    );

    expect(readOutputFile(outputDir, "lib", "clients", "index.ts")).toBe(
      "export const ownedByPlugin = true;\n"
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./lib/clients/index.js"])
    );
  });

  test("includes root-level generated files in the root barrel", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["rootClient.ts", "todo/GetTodoResponse.ts"],
      })
    );

    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./rootClient.js", "./todo/index.js"])
    );
  });

  test("normalizes Windows-style generated paths to POSIX export specifiers", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: [
          "rootClient.ts",
          "todo\\GetTodoRequest.ts",
          "lib\\clients\\BaseClient.ts",
        ],
      })
    );

    expect(readOutputFile(outputDir, "todo", "index.ts")).toBe(
      expectedIndexFile(["./GetTodoRequest.js"])
    );
    expect(readOutputFile(outputDir, "lib", "clients", "index.ts")).toBe(
      expectedIndexFile(["./BaseClient.js"])
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile([
        "./lib/clients/index.js",
        "./rootClient.js",
        "./todo/index.js",
      ])
    );
  });

  test("deduplicates repeated generated files in barrels", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: [
          "todo/GetTodoResponse.ts",
          "todo/GetTodoResponse.ts",
          "rootClient.ts",
          "rootClient.ts",
        ],
      })
    );

    expect(readOutputFile(outputDir, "todo", "index.ts")).toBe(
      expectedIndexFile(["./GetTodoResponse.js"])
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./rootClient.js", "./todo/index.js"])
    );
  });

  test("does not export the root barrel from itself", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["index.ts", "todo/GetTodoResponse.ts"],
      })
    );

    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./todo/index.js"])
    );
  });

  test("ignores generated JSON files when building TypeScript barrels", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["openapi/openapi.json", "todo/GetTodoResponse.ts"],
      })
    );

    expect(fs.existsSync(path.join(outputDir, "openapi", "index.ts"))).toBe(
      false
    );
    expect(readOutputFile(outputDir, "todo", "index.ts")).toBe(
      expectedIndexFile(["./GetTodoResponse.js"])
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(
      expectedIndexFile(["./todo/index.js"])
    );
  });

  test("writes an empty root barrel when only generated JSON files exist", () => {
    const outputDir = createTempOutputDir();
    const openApiFile = path.join(outputDir, "openapi", "openapi.json");
    fs.mkdirSync(path.dirname(openApiFile), { recursive: true });
    fs.writeFileSync(openApiFile, "{}\n");

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: ["openapi/openapi.json"],
      })
    );

    expect(fs.existsSync(path.join(outputDir, "openapi", "index.ts"))).toBe(
      false
    );
    expect(readOutputFile(outputDir, "index.ts")).toBe(expectedIndexFile([]));
    expect(fs.readdirSync(path.join(outputDir, "openapi"))).toEqual([
      "openapi.json",
    ]);
  });

  test("writes an empty root barrel when no files were generated", () => {
    const outputDir = createTempOutputDir();

    generateIndexFiles(
      templateDir,
      aGeneratorContextWith({
        outputDir,
        generatedFiles: [],
      })
    );

    expect(readOutputFile(outputDir, "index.ts")).toBe(expectedIndexFile([]));
    expect(fs.readdirSync(outputDir)).toEqual(["index.ts"]);
  });
});
